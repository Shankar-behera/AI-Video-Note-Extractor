import { useState } from "react";
import toast from "react-hot-toast";
import NotesTab from "./NotesTab";
import SummaryTab from "./SummaryTab";
import FlashcardsTab from "./FlashcardsTab";
import TranscriptTab from "./TranscriptTab";

const TABS = [
  { id: "notes", label: "📝 Notes", icon: "📝" },
  { id: "summary", label: "📋 Summary", icon: "📋" },
  { id: "flashcards", label: "⚡ Flashcards", icon: "⚡" },
  { id: "transcript", label: "🎙️ Transcript", icon: "🎙️" },
];

export default function OutputPanel({ result }) {
  const [activeTab, setActiveTab] = useState("notes");
  const [downloading, setDownloading] = useState(false);

  const handleCopy = async () => {
    const content = {
      notes: result?.notes,
      summary: result?.summary,
      flashcards: JSON.stringify(result?.flashcards, null, 2),
      transcript: result?.transcript,
    }[activeTab];

    if (!content) {
      toast.error("Nothing to copy yet");
      return;
    }
    await navigator.clipboard.writeText(content);
    toast.success("Copied to clipboard!");
  };

  const handleDownloadPDF = async () => {
    if (!result?.notes) {
      toast.error("No notes to download");
      return;
    }
    setDownloading(true);
    toast("Generating PDF...", { icon: "📄" });

    try {
      // Dynamic import to keep bundle small
      const { default: jsPDF } = await import("jspdf");
      const doc = new jsPDF({ unit: "mm", format: "a4" });

      const pageWidth = doc.internal.pageSize.getWidth();
      const margin = 20;
      const maxWidth = pageWidth - margin * 2;
      let y = margin;

      // Title
      doc.setFontSize(20);
      doc.setFont("helvetica", "bold");
      doc.setTextColor(0, 200, 100);
      doc.text("AI Video Notes", margin, y);
      y += 10;

      doc.setFontSize(9);
      doc.setFont("helvetica", "normal");
      doc.setTextColor(120, 120, 140);
      doc.text(`Generated by AI Video Note Extractor — ${new Date().toLocaleDateString()}`, margin, y);
      y += 14;

      // Separator
      doc.setDrawColor(0, 200, 100);
      doc.setLineWidth(0.5);
      doc.line(margin, y, pageWidth - margin, y);
      y += 8;

      // Notes content (strip markdown for PDF)
      const plainNotes = result.notes
        .replace(/#{1,6}\s/g, "")
        .replace(/\*\*(.*?)\*\*/g, "$1")
        .replace(/\*(.*?)\*/g, "$1")
        .replace(/`(.*?)`/g, "$1")
        .replace(/^\s*[-•]\s/gm, "• ")
        .replace(/\|.*\|/g, "")
        .replace(/---+/g, "─".repeat(40));

      doc.setFontSize(10);
      doc.setFont("helvetica", "normal");
      doc.setTextColor(30, 30, 50);

      const lines = doc.splitTextToSize(plainNotes, maxWidth);
      for (const line of lines) {
        if (y > doc.internal.pageSize.getHeight() - margin) {
          doc.addPage();
          y = margin;
        }
        doc.text(line, margin, y);
        y += 5;
      }

      doc.save("ai-video-notes.pdf");
      toast.success("PDF downloaded!");
    } catch (err) {
      console.error(err);
      toast.error("PDF generation failed");
    } finally {
      setDownloading(false);
    }
  };

  return (
    <div
      className="rounded-2xl overflow-hidden"
      style={{ background: "var(--bg-card)", border: "1px solid var(--border)" }}
    >
      {/* Tab header */}
      <div
        className="flex items-center gap-1 px-4 pt-4 pb-0 border-b overflow-x-auto"
        style={{ borderColor: "var(--border)" }}
      >
        <div className="flex items-center gap-1 flex-1">
          {TABS.map((tab) => (
            <button
              key={tab.id}
              onClick={() => setActiveTab(tab.id)}
              className="px-4 py-2.5 text-sm font-medium whitespace-nowrap transition-all duration-200 border-b-2 -mb-px"
              style={{
                fontFamily: "'Syne', sans-serif",
                borderBottomColor: activeTab === tab.id ? "var(--signal)" : "transparent",
                color: activeTab === tab.id ? "var(--signal)" : "var(--text-secondary)",
                background: "transparent",
                borderTop: "none",
                borderLeft: "none",
                borderRight: "none",
              }}
            >
              {tab.label}
            </button>
          ))}
        </div>

        {/* Action buttons */}
        <div className="flex items-center gap-2 pb-1 ml-2 flex-shrink-0">
          <button
            onClick={handleCopy}
            className="px-3 py-1.5 rounded-lg text-xs font-medium transition-all"
            style={{
              background: "rgba(255,255,255,0.04)",
              border: "1px solid var(--border)",
              color: "var(--text-secondary)",
            }}
            title="Copy to clipboard"
          >
            📋 Copy
          </button>
          <button
            onClick={handleDownloadPDF}
            disabled={downloading}
            className="px-3 py-1.5 rounded-lg text-xs font-medium transition-all"
            style={{
              background: "rgba(0,255,135,0.08)",
              border: "1px solid rgba(0,255,135,0.2)",
              color: "var(--signal)",
              opacity: downloading ? 0.6 : 1,
            }}
            title="Download as PDF"
          >
            {downloading ? "⏳ Generating..." : "⬇️ PDF"}
          </button>
        </div>
      </div>

      {/* Tab body */}
      <div className="p-6">
        {activeTab === "notes" && <NotesTab notes={result?.notes} />}
        {activeTab === "summary" && <SummaryTab summary={result?.summary} />}
        {activeTab === "flashcards" && <FlashcardsTab flashcards={result?.flashcards} />}
        {activeTab === "transcript" && <TranscriptTab transcript={result?.transcript} />}
      </div>
    </div>
  );
}
